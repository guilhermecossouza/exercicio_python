print(2 + 3)  # soma
print(2 - 3)  # subtração
print(2 * 3)  # Multiplicação
print(2 / 3)  # Divisão
print(2 // 3)  # divisão inteira
print(2 ** 3)  # potência
print(2 % 3)  # resto da divisão
# ordem
# 1 - () Tudo que for executado dentro dos parênteses
# 2 - ** Potência
# 3 - * - / - // - % nessa ordem que é apresentado
# 4 - + - Nessa ordem de apresentação

a = 1
a = ++a  # operado unário - incremento
print(a)
a = --a  # operado unário - decremento
print(a)
