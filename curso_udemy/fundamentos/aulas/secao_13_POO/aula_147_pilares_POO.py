# Herança
# É a capacidade de herdar comportamentos de outro objeto
# O grande obtivo da Herança é reutilização de código

# Encapsulamento
# E a capacidade de você usar determinado objeto sem entender a parte interna dele
# ex: controle remoto

# Poliformismo
# E a capacidade de substituir um determinado elemento concreto por um coneito abstrato


